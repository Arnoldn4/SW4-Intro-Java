public class WhileLoop {
    public static void main(String[] args) {
      // while loop : execute the statement as long as condition is true
      /*
      for (int counter=1; counter <=20;
       */
        int x =1;
        while (x <=20){
            System.out.println("While loop counter:" + x);
            x++; // update the counter

        }
    }
}
